add_filter( 'gettext', 'nerv_core_zh_cn_gettext_fallback', 20, 3 );

function nerv_core_should_use_zh_cn_fallback(): bool {
	$locale = function_exists( 'determine_locale' ) ? determine_locale() : get_locale();
	return 0 === strpos( (string) $locale, 'zh_CN' );
}

function nerv_core_zh_cn_gettext_fallback( string $translation, string $text, string $domain ): string {
	if ( 'nerv-core' !== $domain || ! nerv_core_should_use_zh_cn_fallback() ) {
		return $translation;
	}

	$fallback = nerv_core_zh_cn_fallback_translations();
	return $fallback[ $text ] ?? $translation;
}

function nerv_core_admin_i18n_payload(): array {
	return nerv_core_should_use_zh_cn_fallback() ? nerv_core_zh_cn_fallback_translations() : array();
}

function nerv_core_zh_cn_js_locale_data(): array {
	$messages = array(
		'' => array(
			'domain' => 'nerv-core',
			'lang'   => 'zh_CN',
		),
	);

	foreach ( nerv_core_admin_i18n_payload() as $source => $translation ) {
		$messages[ $source ] = array( $translation );
	}

	return $messages;
}

